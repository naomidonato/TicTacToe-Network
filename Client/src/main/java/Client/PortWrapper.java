package Client;

//************************************************************************************
public class PortWrapper {

    //***************************************************************************
    private int port;
    private static final int PORT_MIN = 1;
    private static final int PORT_MAX = 65535;
    //***************************************************************************


    //***************************************************************************
    PortWrapper(int port) {
        this.port = port;
    }
    //***************************************************************************


    //***************************************************************************
    @Override
    public String toString() {
        return Integer.toString(port);
    }
    public int getPort() {
        return port;
    }
    //***************************************************************************


    //***************************************************************************
    public static boolean isValidPort(int port) {
        return isValidPort(new PortWrapper(port));
    }
    //***************************************************************************


    //***************************************************************************
    public static boolean isValidPort(PortWrapper pw) {

        if(pw.getPort() >= PORT_MIN && pw.getPort() <= PORT_MAX) {
            return true;
        }
        else {
            return false;
        }
    }
    //***************************************************************************
}
//************************************************************************************