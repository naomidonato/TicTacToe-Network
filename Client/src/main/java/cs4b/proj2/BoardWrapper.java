package Client;
import java.io.Serializable;




//************************************************************************************
public class BoardWrapper implements Serializable {

    //***************************************************************************
    private P_FLAGS flag;
    private Board board;
    //***************************************************************************


    //***************************************************************************
    public BoardWrapper(P_FLAGS flag, Board board) {
        this.flag = flag;
        this.board = board;
    }
    //***************************************************************************


    //***************************************************************************
    public P_FLAGS getFlag() {
        return flag;
    }
    //***************************************************************************


    //***************************************************************************
    public Board getBoard() {
        return board;
    }
    //***************************************************************************
}
//************************************************************************************